<h1 class="titulo">Beato Carlo Acutis</h1>
<br />

<h3 class="subtitulo">
    Infância e adolescência
</h3>
<p class="paragrafonormal">
    Carlo Acutis nasceu em 3 de maio de 1991, em Londres, porque seus pais estavam lá a trabalho. 
    Ele também foi batizado nesta mesma cidade, no dia 18 de maio, mas logo a família voltou para sua cidade 
    de origem, Milão, na Itália, onde Carlo cresceu. Seus pais, Andrea e Antonia, viviam muito bem 
    economicamente e, apesar de se considerarem católicos, não praticavam a religião — não tinham 
    uma vida católica.
</p>
<p class="paragrafonormal">
    Desse modo, a vida espiritual de Carlo ganhou impulso significativo por influência de sua babá polonesa, 
    de nome Beata, que era profundamente devota. Ela lhe ensinou as primeiras orações e o introduziu à fé 
    católica de modo que, com cerca de 4 anos de idade, Carlo passou a demonstrar grande interesse pelas 
    coisas de Deus.
</p>
<p class="paragrafonormal">
    O menino fazia muitas perguntas à sua mãe, que passou a estudar sobre a fé católica para responder às 
    dúvidas do filho. E, com isso, ela voltou à prática da religião. Carlo <b> frequentava a missa diariamente, 
    participava da Adoração Eucarística e rezava o Rosário.</b> Além disso, ele realizava serviços aos pobres, 
    e apesar de ter nascido em uma família rica, era bastante desapegado de seus bens, sempre pensando 
    naqueles que pouco tinham.
</p>
<p class="text-body-secondary">
    -Em casa, pedia para colocar a sobra de comida em recipientes, para então levá-la aos desabrigados 
    locais. “[…] costumava ir com os pais pelas ruas de Milão, para distribuir cobertores e refeições 
    quentes aos desabrigados”. Dava a justa medida ao dinheiro e se zangava quando queriam comprar-lhe 
    um segundo par de sapatos.[…]  “ele tinha o hábito de juntar as ajudas semanais que lhe eram dadas 
    pela família, para entregá-las aos necessitados da Obra de São Francisco em Milão”.
</p>
<p class="paragrafonormal">
    Além disso, Carlo Acutis tinha uma compreensão espiritual impressionante para sua idade e <b>falava a 
    todos com naturalidade sobre sua amizade com Jesus.</b> Inclusive, pessoas que trabalhavam na casa da sua 
    família se converteram à fé católica por ouvi-lo falar da Eucaristia. Tamanho era seu amor pelo Corpo 
    de Cristo que, com 7 anos de idade, manifestou a vontade de receber a Santa Comunhão. E, de tanto 
    insistir, seus pais o apresentaram ao bispo que, ao conhecê-lo, permitiu que assim acontecesse, pois 
    percebeu que aos 7 anos o menino já tinha uma fé madura o suficiente para receber Jesus na Eucaristia.
</p>

<h3 class="subtitulo">
    As principais devoções de Carlo Acutis
</h3>
<p class="paragrafonormal">
    Os dois grandes pilares da vida espiritual de Carlo eram a <b> Eucaristia e Nossa Senhora</b>, tinha particular 
    devoção ao Rosário. Desse modo, ele chamava a Eucaristia de “a minha estrada para o céu” e se confessava 
    regularmente para manter-se em estado de graça. Carlo entendia, de fato, o poder transformador da 
    Eucaristia e dedicava seu tempo para participar da adoração eucarística sempre que possível.
</p>
<p class="paragrafonormal">
    O Rosário, para Carlo, era uma poderosa arma espiritual. Ele era consagrado a Nossa Senhora e acreditava 
    firmemente no valor da intercessão da Virgem Maria. Além disso, incentivava todos ao seu redor rezarem o 
    Rosário. Sua prática de fé era simples e profunda, inspirando a todos que tinham a sorte de conhecê-lo. 
</p>

<h3 class="subtitulo">
    Apóstolo na internet
</h3>
<p class="paragrafonormal">
    Outra questão muito presente na vida de Carlo Acutis foi a internet. Ele tinha uma paixão pela 
    tecnologia e, ainda muito jovem, percebeu o potencial da internet como uma ferramenta para evangelização. 
    Assim, passou a utilizá-la para realizar um lindo apostolado, manifestando ainda mais o seu amor pela 
    Eucaristia e por Nossa Senhora. 
</p>
<p class="paragrafonormal">
    Desse modo, aos 11 anos, ele começou a criar um site dedicado a catalogar milagres eucarísticos ao redor 
    do mundo. Sua pesquisa foi espantosamente extensa, ele dedicou muito esforço para colher materiais, 
    inclusive pedia aos seus pais que o levassem a determinados lugares para obter conteúdos para sua 
    pesquisa. E, assim, ele conseguia fotos, folhetos, informações — muitas coisas que ainda nem estavam 
    disponíveis na internet na época.
</p>
<p class="text-body-secondary">
    -“Ele era um gênio – diz Gori – porque mesmo não tendo completado os estudos especializados, era capaz de 
    criar programas de computador melhor que os acadêmicos e de usar as mídias sociais com o objetivo de 
    evangelização e promoção humana”. 
</p>
<p class="paragrafonormal">
    De fato, Carlo tinha um talento especial para tornar a fé mais acessível e cativante, usando a linguagem 
    e os meios disponíveis a sua geração. Ele acreditava que a internet poderia ser um espaço para espalhar a 
    fé. Sendo assim, fazia questão de apresentar, sobretudo aos jovens, a beleza da fé, com argumentos sólidos 
    para convertê-los e levá-los a enxergar a verdade que ele já via. Carlo queria tornar conhecidos os 
    milagres que apontavam para o sobrenatural presente na Eucaristia.
</p>
<p class="paragrafonormal">
    Além disso, ele fez outra pesquisa, dessa vez sobre as <b>aparições de Nossa Senhora</b>, a fim de torná-las 
    ainda mais conhecidas através de seu site. Carlo tinha uma devoção especial a Nossa Senhora de Fátima e 
    Nossa Senhora de Lourdes. Ele lia muito sobre as aparições e sobre a vida dos santos, a ponto de deixar 
    que aqueles trechos penetrassem o seu coração e transformasse a sua vida de modo que passou a oferecer os 
    seus sofrimentos a Jesus e rezar muito mais, a fim de alcançar o céu.
</p>
<p class="text-body-secondary">
    -A Eucaristia, o centro de sua jovem vida de 15 anos, seu serviço aos pobres junto à sua paixão pela web – 
    tanto que muitos o consideram o santo padroeiro da internet – tornam a história do Beato cheia de “coisas 
    do mundo”, mas com um horizonte claro, o da eternidade. 
</p>

<h3 class="subtitulo">
    Padroeiro da internet
</h3>
<p class="paragrafonormal">
    A Igreja tem Santa Clara como padroeira da televisão e, agora, séculos depois, Carlo Acutis é oficialmente 
    declarado padroeiro da internet! Este título reflete sua paixão pela tecnologia e a sua habilidade em 
    usá-la para o Bem maior. Ele é um modelo para todos os jovens que desejam usar suas habilidades e talentos 
    para servir a Deus e à Igreja. Carlo provou que a santidade não está reservada aos mosteiros ou aos tempos 
    antigos, mas pode florescer em meio à era digital.
</p>
<p class="text-body-secondary">
    -“Ele era um gênio – diz Gori – porque mesmo não tendo completado os estudos especializados, era capaz de 
    criar programas de computador melhor que os acadêmicos e de usar as mídias sociais com o objetivo de 
    evangelização e promoção humana”.
</p>
<p class="paragrafonormal">
    Seu legado continua vivo, inspirando jovens e adultos a buscar uma vida de fé autêntica e ativa. Carlo 
    Acutis lembra-nos que cada um de nós tem a capacidade de evangelizar o mundo, começando pelos que estão ao 
    nosso redor, através do amor a Deus e ao próximo. Seu exemplo é um convite apara abraçarmos a fé com 
    alegria e criatividade, usando todos os recursos à nossa disposição para glorificar a Deus.
</p>

<h3 class="subtitulo">
    Morte e processo de beatificação
</h3>
<p class="paragrafonormal">
    Dois meses antes de sua morte, e quando ainda não havia descoberto a doença, Carlo gravou um pequeno vídeo 
    dizendo “Estou destinado a morrer” e, de certa forma, despede-se. Parece mesmo que ele já sabia que logo 
    iria morrer e, aos 15 anos, foi diagnosticado com leucemia. Além disso, seu tipo de câncer era o mais 
    grave — restava-lhe apenas poucos dias de vida.
</p>
<p class="paragrafonormal">
    Mesmo enfrentando uma doença tão agressiva, ele não perdeu a fé nem o espírito alegre. Além disso, 
    oferecia os seus sofrimentos a Deus e pensava mais nos outros que em si mesmo, demonstrando uma grande 
    maturidade espiritual.
</p>
<p class="text-body-secondary">
    Tão logo baixou o hospital, disse aos pais: “Ofereço os sofrimentos que deverei sofrer ao Senhor, pelo Papa 
    e pela Igreja, para não ir ao Purgatório e ir direto ao céu”. E diante dos sofrimentos, procurava minimizar. 
    “Há pessoas que sofrem muito mais do que eu”, respondia às enfermeiras que lhe perguntavam como ele estava. 
    E acrescentava: “não acorde a mãe que está cansada e se preocuparia mais”.
</p>
<p class="paragrafonormal">
    Ele faleceu em 12 de outubro de 2006, deixando um legado de santidade e exemplo para jovens do mundo 
    inteiro. No dia do seu funeral, muitas pessoas compareceram — entre elas moradores de rua, que Carlos 
    ajudava sem que ninguém soubesse — a ponto de deixar seus familiares admirados
</p>
<p class="paragrafonormal">
    O processo de beatificação de Carlo Acutis começou oficialmente em 15 de fevereiro de 2013, quando foi 
    declarado Servo de Deus, o primeiro passo formal rumo à santidade. Esta etapa inicial reconheceu 
    oficialmente suas virtudes heroicas e permitiu que a Igreja começasse a investigar sua vida e obras em 
    profundidade. Em 5 de julho de 2018, o Papa Francisco declarou Carlo Venerável, reconhecendo suas virtudes 
    heroicas e destacando sua vida como um exemplo extraordinário de santidade.
</p>
<p class="paragrafonormal">
    A beatificação de Carlo Acutis foi um marco significativo e aconteceu em 10 de outubro de 2020, na Basílica 
    de São Francisco de Assis. Este evento reconheceu um milagre atribuído à sua intercessão: a cura milagrosa 
    de um menino brasileiro, de Campo Grande (MS), que sofria de uma anomalia rara no pâncreas. A cerimônia de 
    beatificação, presidida pelo cardeal Agostino Vallini, foi um momento de alegria e esperança para muitos, 
    especialmente para os jovens, que viram em Carlo um modelo contemporâneo de santidade.
</p>
<h4 class="subsubtitulo">
    Onde está o corpe de Carlo Acutis?
</h4>
<p class="paragrafonormal">
    O corpo de Carlo Acutis foi enterrado em Assis, como ele queria — na terra de São Francisco de Assis. 
    Embora não morasse em Assis, a família de Carlo possuía uma casa na cidade e, por isso, tinha o direito a 
    um lugar no cemitério da cidade. Anos depois, em 23 de janeiro de 2019, seu corpo foi exumado e, em seguida, 
    transladado para o Santuário da Espoliação, também em Assis, onde agora está exposto para veneração pública.
</p>
<p class="paragrafonormal">
    Além disso, seu corpo, apesar de não estar incorrupto, encontra-se em um estado de conservação 
    impressionante. Ele também está vestido com roupas simples: jeans e um par de tênis, refletindo sua vida 
    cotidiana. Este traslado tornou Carlo ainda mais acessível aos fiéis que visitam Assis, proporcionando um 
    local de peregrinação e oração.
</p>

<h3 class="subtitulo">
    Carlos Acutis é reconhecido pelo Vaticano para a canonização
</h3>
<p class="paragrafonormal">
    O reconhecimento de um santo é sempre um momento de grande alegria para a Igreja Católica. Eles são nossos 
    modelos de virtude, além de intercessores poderosos, mostrando-nos como viver a fé de maneira autêntica e 
    profunda. Especialmente emocionante é a canonização de jovens, demonstrando que a santidade é acessível a 
    todas as idades e circunstâncias da vida.
</p>
<p class="paragrafonormal">
    Em 23 de maio, o Papa Francisco recebeu em audiência o prefeito do Dicastério das Causas dos Santos, 
    Cardeal Marcello Semeraro, e autorizou a publicação de decretos que incluíam o reconhecimento de um 
    milagre atribuído à intercessão do Beato Carlo Acutis. Este passo crucial abre caminho para a canonização 
    de Carlo, que será oficialmente declarado santo.
</p>
<p class="paragrafonormal">
    O milagre que permite a canonização de Carlo Acutis envolveu a cura milagrosa de Valeria Valverde, uma 
    jovem de 21 anos da Costa Rica. Ela sofreu um grave acidente de bicicleta enquanto estudava em Florença, 
    Itália, em 2022. E, em busca de ajuda do céu, a mãe de Valeria visitou o túmulo do Beato Carlo Acutis para 
    pedir sua intercessão. Dias depois, a contusão hemorrágica no cérebro de Valeria desapareceu completamente 
    — uma recuperação inexplicável pelos médicos.
</p>
<p class="paragrafonormal">
    Segundo fontes oficiais, a canonização acontecerá no dia 27 de abril de 2025. sem dúvida, será um evento de
    celebração não apenas de sua vida, mas também da esperança e da inspiração que ele oferece às futuras 
    gerações. Carlo Acutis nos ensina que, com amor, dedicação e devoção, qualquer um de nós pode seguir o 
    caminho da santidade.
</p>

